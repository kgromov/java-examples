   public static CountryCodeNames getCountryCodeNamesByCountryCode(long countryCode)
   {
     for (CountryCodeNames countryCodeNames : CountryCodeNames.values())
     {
       if (isFound(countryCode, countryCodeNames))
       {
         return countryCodeNames;
       }
     }
     return CountryCodeNames.UNDEFINED;
   }

   private static boolean isFound(long countryCode, CountryCodeNames countryCodeNames)
   {
     return countryCodeNames.getCountryCodes()
         .stream()
         .anyMatch(code -> code == countryCode);
   }

